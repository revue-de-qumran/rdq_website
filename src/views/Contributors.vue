<h2>Articles and Notes</h2>

<p>Articles, but not Short Notes, should include an summary of between 100 and 200 words in French and English placed at the beginning of the article. Please supply full contact details including a short biography with institutional affiliation. Revue de Qumrân publishes affiliation and academic address of each author in each issue.</p>

<p>For submission and to facilitate anonymous refereeing, the author's name and institution should not appear in the submited article. Attention should be paid also on self-references, acknowledgments, or the metadata of electronic document, etc.</p>

<p>Revue de Qumrân uses a double-blind peer review system. The Journal committee undertakes to provide a reply within reasonable time.</p>

<p>English articles should follow the SBL Handbook of Style (2nd Edition) and Chicago Manual of Style. Recent issues of the Journal are the best guide to its stylistic and scholarly conventions. </p>

<p>
<b>Examples:</b><br />
<b>For footnotes:</b><br /> 
Eric D. Reymond, “<i>Vav</i> and <i>Yod</i> in the Hebrew Manuscripts A and B of Ben Sira,” in <i>Sirach and Its Contexts: The Pursuit of Wisdom and Human Flourishing</i>, ed. Samuel L. Adams, Greg Schmidt Goering, and Matthew J. Goff, JSJSup 196 (Leiden: Brill, 2021), 104–24.<br />
<b>For bibliography:</b><br />
Reymond, Eric D. “<i>Vav</i> and <i>Yod</i> in the Hebrew Manuscripts A and B of Ben Sira.” Pages 104–24 in <i>Sirach and Its Contexts: The Pursuit of Wisdom and Human Flourishing</i>. Edited by Samuel L. Adams, Greg Schmidt Goering, and Matthew J. Goff. JSJSup 196. Leiden: Brill, 2021. <br />
</p>

<p>Use en-dash (–) between page numbers. Use em-dash (—) where appropriate within text. Do not use a sequence of dashes (-- or ---) for either en-dash or em-dash.</p>


